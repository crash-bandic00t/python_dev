/* Задание 1.
С помощью цикла for написать алгоритм для вывода чисел (выводите в консоль, с помощью console.log) от 0 до 10
включительно.
*/
'use strict'
console.log('0 - это ноль') // можно вывести и в цикле при помощи if, но в данном случае так будет проще.
for (let i = 1; i <= 10; i++) {
    if (i % 2 == 0) {
        console.log(`${i} - это четное число`)
    } else {
        console.log(`${i} - это нечетное число`)
    }
}