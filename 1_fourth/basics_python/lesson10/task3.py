"""
Осуществить программу работы с органическими клетками, состоящими из ячеек.
Необходимо создать класс «Клетка». В его конструкторе инициализировать параметр, соответствующий количеству ячеек клетки (целое число). В классе должны быть реализованы методы перегрузки арифметических операторов:
сложение (__add__()), вычитание (__sub__()), умножение (__mul__()), деление (__truediv__()). Эти методы должны применяться только к клеткам и выполнять увеличение, уменьшение, умножение и округление до целого числа деления клеток, соответственно.
Сложение. Объединение двух клеток. При этом число ячеек общей клетки должно равняться сумме ячеек исходных двух клеток.
Вычитание. Участвуют две клетки. Операцию необходимо выполнять, только если разность количества ячеек двух клеток больше нуля, иначе выводить соответствующее сообщение.
Умножение. Создаётся общая клетка из двух. Число ячеек общей клетки — произведение количества ячеек этих двух клеток.
Деление. Создаётся общая клетка из двух. Число ячеек общей клетки определяется как целочисленное деление количества ячеек этих двух клеток.
Добавить к классу метод print(columns), распечатыващий на экране звездочки рядами по columns звездочек в одном ряду в количестве равном числу ячеек клетки.
Если ячеек на формирование ряда не хватает, то в последний ряд записываются все оставшиеся.
Например, если в клетке 12 ячеек, а запросили напечатать по 5 звездочек в ряду, то на экране должно быть:

*****
*****
**
"""
class Cell:
    def __init__(self, cell_cnt):
        try:
            self.cell_cnt = int(cell_cnt)
        except:
            raise ValueError('Введите число!')
    
    def __add__(self, other):
        return Cell(self.cell_cnt + other.cell_cnt)

    def __sub__(self, other):
        tmp = self.cell_cnt - other.cell_cnt
        if tmp <= 0:
            raise ValueError('Клетка уничтожена! Вы вычли из меньшей клетки большую!')
        else:
            return Cell(tmp)

    def __mul__(self, other):
        return Cell(self.cell_cnt * other.cell_cnt)

    def __truediv__(self, other):
        return Cell(self.cell_cnt / other.cell_cnt)
    
    def print(self, columns):
        cnt = 1
        for i in range(1, self.cell_cnt + 1):
            if i == columns * cnt:
                print('*')
                cnt += 1
            elif i == self.cell_cnt:
                print('*')
            else:
                print('*', end='')



a = Cell(16)
b = Cell(15)
a.print(7)
print((a + b).cell_cnt)
print((a - b).cell_cnt)
print((a * b).cell_cnt)
print((a / b).cell_cnt)
