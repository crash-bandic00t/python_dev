# Посчитать четные и нечетные цифры введенного натурального числа.
# Например, если введено число 34560, то у него 3 четные цифры (4, 6 и 0) и 2 нечетные (3 и 5).

number = int(input('Введите число: '))

cntOdd = 0
cntEven = 0

for i in map(int, str(number)): #переводим число в строку и итерируемся по ней
    if i % 2 == 0:
        cntEven += 1
    else:
        cntOdd += 1

print(f'В числе "{number}" {cntOdd} нечетных(ая) цифр(ы, а) и {cntEven} четных(ое, ая)')

